#include "Satur.h"
#include <cmath>

// Проверка переполнения (если |число| > 9999)
void Satur::checkflag() {
    // Если экспонента больше 4, значит число > 9999
    if (exp > 4) {
        ValueFlag = 1;           // устанавливаем флаг переполнения
        // Заменяем на максимальное значение с сохранением знака
        if (mant < 0) {
            mant = -9999;        // минимальное значение
        }
        else {
            mant = 9999;         // максимальное значение
        }
        exp = 4;                 // фиксируем экспоненту
    }
    else {
        ValueFlag = 0;           // переполнения нет
    }
}

// Вывод числа: если было переполнение, выводим звездочку
void Satur::print() {
    if (ValueFlag == 1) {
        printf("*");
    }
    fl::print();  // вызываем вывод базового класса
}

// Конструктор по умолчанию (создает число 0)
Satur::Satur() : fl(0.0) {
    ValueFlag = 0;
}

// Конструктор от мантиссы и экспоненты
Satur::Satur(int n, int d) : fl(n, d) {
    checkflag();  // проверяем переполнение
}

// Конструктор от вещественного числа
Satur::Satur(double d) : fl(d) {
    checkflag();  // проверяем переполнение
}

// Конструктор от объекта базового класса
Satur::Satur(fl f) : fl(f) {
    checkflag();  // проверяем переполнение
}

// Оператор присваивания
Satur& Satur::operator=(const Satur& other) {
    if (this != &other) {               // защита от самоприсваивания
        mant = other.mant;              // копируем мантиссу
        exp = other.exp;                // копируем экспоненту
        ValueFlag = other.ValueFlag;    // копируем флаг
    }
    return *this;
}
